function vector=build_indep_q_manual(vaux,sigma,p)

global alpha lambda mu c0

vector=zeros(4,1);

k1=alpha/(16*(lambda+mu));
k2=(c0+alpha^2/(lambda+mu))/4;

vector(1)=vaux(1)+k1*(sigma(2)+sigma(3)+sigma(7)+sigma(10)+...
                      sigma(15)+sigma(18)+sigma(21)+sigma(28)) + ...
                  k2*p(1);
vector(2)=vaux(2)+k1*(sigma(6)+sigma(7)+sigma(11)+sigma(14)+...
                      sigma(21)+sigma(24)+sigma(29)+sigma(34)) + ...
                  k2*p(2);
vector(3)=vaux(3)+k1*(sigma(18)+sigma(19)+sigma(25)+sigma(28)+...
                      sigma(35)+sigma(38)+sigma(39)+sigma(44)) + ...
                  k2*p(3);
vector(4)=vaux(4)+k1*(sigma(24)+sigma(25)+sigma(31)+sigma(34)+...
                      sigma(39)+sigma(42)+sigma(45)+sigma(48)) + ...
                  k2*p(4);

end